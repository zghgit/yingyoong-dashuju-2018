        if (prop !== 'trade_ratio' && prop !== 'sec_name' && prop !== 'sec_code' && prop !== 'stat_date' && prop !== 'mem_full_name') {
          return formatTbousandth(cellValue)
        } else {
          return cellValue
        }
      },
    }
