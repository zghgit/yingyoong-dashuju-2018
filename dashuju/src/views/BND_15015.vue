      <!--<p>-->
        <!--无-->
      <!--</p>-->
    </Modal>
    <div class="tableBox tab">
      <p id="infoTableList" class="redtext listlength"></p>
      <el-table
        :data="tableData1"
        border
        style="width: 100%">
        <template v-for="(item,index) in tableColumns1">
          <el-table-column v-if="item.hasChildren == true" :key="index"
                           :prop="item.field"
                           :label="item.title"
                           :align="item.align"
                           :width="item.width">
            <template scope="scope">
              <div class="slotLeft">{{scope.row.variety}}</div>
